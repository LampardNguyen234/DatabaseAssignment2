CREATE OR REPLACE PROCEDURE TINH_TIEN_DOT_KHAM(mMA_DOT_DIEU_TRI IN VARCHAR2, mMA_BENH_NHAN IN VARCHAR2 , mTONG_TIEN OUT INT)
IS 
mTIEN_THUOC INT;
mTIEN_GIUONG INT;
mLOAI_BN INT;
mHOURS INT;
mDAY INT;
mSTART TIMESTAMP;
mEND TIMESTAMP;
mGIA INT;
BEGIN
    SELECT LOAI_BN
    INTO mLOAI_BN
    FROM BENH_NHAN
    WHERE MA_BN = mMA_BENH_NHAN;
    IF(mLOAI_BN = 1) THEN
        SELECT SUM(J.SO_LUONG*W.DON_GIA)
        INTO mTIEN_THUOC
        FROM KE_THUOC_NOI_TRU J , THUOC W
        WHERE J.MA_DOT = mMA_DOT_DIEU_TRI AND J.MA_BN = mMA_BENH_NHAN AND J.MA_THUOC = W.MA_THUOC;

        SELECT BAT_DAU
        INTO mSTART
        FROM DOT_DIEU_TRI
        WHERE MA_BN = mMA_BENH_NHAN AND MA_DOT = mMA_DOT_DIEU_TRI;

        SELECT KET_THUC
        INTO mEND
        FROM DOT_DIEU_TRI
        WHERE MA_BN = mMA_BENH_NHAN AND MA_DOT = mMA_DOT_DIEU_TRI;

        select abs(24*extract(day from diff)+extract(hour from diff)) INTO mHOURS from (
        select
              to_timestamp(  mSTART, 'dd-mon-yyyy hh:mi:ssxff PM tzh:tzm') -
              to_timestamp(   mEND , 'dd-mon-yyyy hh:mi:ssxff PM :tzm') diff
         from dual  );

         mDAY := ROUND(mHOURS/24)+1;

         SELECT MUC_GIA
         INTO mGIA
         FROM GIUONG J, PHONG_NGHO_BN W
         WHERE J.MA_BN_DANG_NAM = mMA_BENH_NHAN AND J.MA_PHONG = W.MA_PHONG AND J.MA_KHU = W.MA_KHU;

         mTIEN_GIUONG := mGIA*mDAY;
         mTONG_TIEN := mTIEN_GIUONG + mTIEN_THUOC;
    END IF;
END;

